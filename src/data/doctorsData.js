function buildSlots(doctorId, online) {
  const numId = Number(doctorId) || 1;
  const startHour = 8 + (numId % 6);
  const lateHour = 15 + (numId % 4);
  const slots = [
    `${String(startHour).padStart(2, "0")}:00`,
    `${String(startHour).padStart(2, "0")}:30`,
    `${String(startHour + 1).padStart(2, "0")}:00`,
    `${String(startHour + 2).padStart(2, "0")}:30`,
    `${String(lateHour).padStart(2, "0")}:00`,
  ];

  if (online) {
    slots.push(`${String(lateHour + 1).padStart(2, "0")}:30 (онлайн)`);
  } else {
    slots.push(`${String(lateHour + 1).padStart(2, "0")}:30`);
  }

  return slots;
}

function buildServices(doctor) {
  const options = specialtyServices[doctor.specialty] || ["Преглед", "Консултация", "Контролен преглед"];
  const startIndex = Number(doctor.id) % options.length;

  return [0, 1, 2].map((offset) => options[(startIndex + offset) % options.length]);
}

function buildBio(doctor) {
  const numId = Number(doctor.id) || 1;
  const years = 6 + (numId % 19);
  const start = bioStarts[numId % bioStarts.length];
  const approach = bioApproaches[numId % bioApproaches.length];
  const focus = specialtyFocus[doctor.specialty] || "прегледи и консултации";

  return [
    `${start} по ${doctor.specialty} с над ${years} години практика в ${doctor.city}.`,
    `Работи в ${doctor.clinicName || "медицински център"} и се фокусира върху ${focus}.`,
    approach,
  ].join(" ");
}

function buildDoctorName(index) {
  const first = firstNames[index % firstNames.length];
  const last = lastNames[(index * 3) % lastNames.length];
  return `Д-р ${first} ${last}`;
}

const DOCTORS_PER_CLINIC = 6;

export const doctors = (() => {
  const list = [];
  let index = 0;

  Object.entries(clinicFilters).forEach(([clinicId, clinic]) => {
    for (let i = 0; i < DOCTORS_PER_CLINIC; i += 1) {
      index += 1;
      const specialty = clinic.specialties[i % clinic.specialties.length] || "Обща медицина";
      const doctor = {
        id: String(index),
        name: buildDoctorName(index),
        specialty,
        city: clinic.city,
        clinicId,
        clinicName: clinic.name,
        online: (index + i) % 2 === 0,
      };

      list.push({
        ...doctor,
        bio: buildBio(doctor),
        services: buildServices(doctor),
        slots: buildSlots(doctor.id, doctor.online),
      });
    }
  });

  return list;
})();
